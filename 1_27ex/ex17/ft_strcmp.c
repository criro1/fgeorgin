int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	if (s1[0] != '\0' && s2[0] == '\0')
		return (1);
	else if (s1[0] == '\0' && s2[0] != '\0')
		return (-1);
	else
	{
		while (s1[i] != '\0' && s2[i] != '\0')
		{
			if (s1[i] > s2[i])
				return (1);
			else if (s1[i] < s2[i])
				return (-1);
			else if (s1[i + 1] != '\0' && s2[i + 1] == '\0')
				return (1);
			else if (s1[i + 1] == '\0' && s2[i + 1] != '\0')
				return (-1);
			i++;
		}
	}
	return (0);
}
