#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t s;

	i = 0;
	s = 0;
	while (s1[i] != '\0')
		i++;
	s = 0;
	while (s < n && s2[s] != '\0')
	{
		s1[i + s] = s2[s];
		s++;
	}
	s1[i + s] = '\0';
	return (s1);
}
