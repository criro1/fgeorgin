#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t i;

	if ((size_t)ft_strlen(dst) >= size)
		return ((size_t)ft_strlen(src) + size);
	i = 0;
	while ((dst[i] != '\0') && i < (size - 1))
		i++;
	while (*src && i < (size - 1))
	{
		dst[i] = *src;
		i++;
		src++;
	}
	dst[i] = '\0';
	return ((size_t)ft_strlen(src) + (size_t)ft_strlen(dst));
}
